function parseVal(value){

    return parseFloat(value);
}

console.log(parseVal(20));

console.log(parseVal('20'));

console.log(parseVal(4.567));

console.log(parseVal('2788.909'));

console.log(parseVal("ashs"));

console.log(parseVal(undefined));

console.log(parseVal(null));

console.log(parseVal(NaN));
